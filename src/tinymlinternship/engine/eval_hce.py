def _pst_square(square: int, color: bool) -> int:
    if color == chess.WHITE:
        return square
    return chess.square_mirror(square)


def evaluate_hce(board: chess.Board) -> int:
    """Static evaluation in centipawns (White = positive)."""
    if board.is_checkmate():
        return -MATE_SCORE if board.turn == chess.WHITE else MATE_SCORE
    if board.is_stalemate() or board.is_insufficient_material():
        return 0

    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece is None:
            continue

        value = PIECE_VALUES[piece.piece_type]
        pst = _PST_BY_TYPE[piece.piece_type][_pst_square(square, piece.color)]
        piece_score = value + pst

        if piece.color == chess.WHITE:
            score += piece_score
        else:
            score -= piece_score

    return score